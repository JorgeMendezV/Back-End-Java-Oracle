package FirstCourseAlura;

public class EjercicioTablaMultiplicacion {
	
	public static void main(String[] args) {
		
		for (int contador = 2; contador <= 2 ; contador++) {
			for (int multiplicacion = 1; multiplicacion <= 10; multiplicacion ++) {
				System.out.print(" ");
				System.out.println(contador + " X "+ multiplicacion 
						+ " : " + contador * multiplicacion);
			}
			System.out.println();
		}
		
	}

}
